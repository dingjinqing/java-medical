<template>
  <div class="payConfigure">
    <!-- 四种支付配置内容区域 -->
    <el-card class="payCard">
      <div
        v-for="item in payConfigure"
        :key="item.payName"
        class="payContent"
      >
        <span>{{item.payName}}</span>
        <el-switch
          v-model="item.enabled"
          active-color="#13ce66"
          inactive-color="#ff4949"
          style="margin: 0 10px;"
        ></el-switch>
        <span>{{item.enabled?'已开启':'已关闭'}}</span>
        <span>{{item.title}}</span>
        <span
          v-if="item.payName==='微信支付'"
          class="setting"
          @click="handleSetting"
        >配置</span>
      </div>
    </el-card>

    <!-- 默认支付配置内容区域 -->
    <div class="defaultPayCongigure">
      <div class="title">
        <span></span>
        默认支付配置
      </div>
      <div class="payMethod">
        <el-checkbox v-model="card_first">会员卡余额支付</el-checkbox>
        <el-checkbox v-model="balance_first">余额支付</el-checkbox>
        <el-checkbox v-model="score_first">积分支付</el-checkbox>
      </div>
      <div class="tips">
        <span>注：</span>
        <span>1、用户在结算时将会默认优先使用以上勾选的支付方式</span>
        <br>
        <span class="secondTips">2、如果勾选以上全部支付方式，计算优先级为会员卡余额>余额支付>积分支付</span>
        <span style="margin-left: 10px;">
          <el-popover
            placement="right-start"
            width="220"
            trigger="hover"
          >
            <el-image :src="src"></el-image>
            <el-button
              slot="reference"
              type="text"
            >查看示例</el-button>
          </el-popover>
        </span>
      </div>
    </div>
    <div class="btn" @click="updateConfig">保存</div>

    <!-- 微信支付配置弹窗 -->
    <el-dialog
      title="微信支付配置"
      :visible.sync="showSettingDialog"
      width=50%
      center
    >
      <ul class="settingContent">
        <li>
          <span>appid：</span>
          <el-input size="small"></el-input>
        </li>
        <li>
          <span>商户号：</span>
          <el-input size="small"></el-input>
        </li>
