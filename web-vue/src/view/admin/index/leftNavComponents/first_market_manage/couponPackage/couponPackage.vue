            ></el-input>
          </div>
          <div class="filters_item"><span>礼包名称：</span>
            <el-input
              v-model="packName"
              placeholder="请输入礼包名称"
              size="small"
            ></el-input>
          </div>
          <div class="filters_item"><span>领取方式：</span>
            <el-select
              v-model="accessMode"
              placeholder="请选择"
              size="small"
            >
              <el-option
                v-for="item in get_type_option"
                :key="item.value"
                :label="item.label"
                :value="item.value"
              ></el-option>
            </el-select>
          </div>
          <div class="filters_item">
            <el-button
              type="primary"
