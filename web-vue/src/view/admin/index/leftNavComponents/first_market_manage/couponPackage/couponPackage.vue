<script>
import { couponPackageList, updateCouponPackage, deleteCouponPackage } from '@/api/admin/marketManage/couponPackage.js'
import statusTab from '@/components/admin/status/statusTab'
// 引入分页
import pagination from '@/components/admin/pagination/pagination'
export default {
  components: { pagination, statusTab },
  data () {
    return {
      activityName: '优惠券礼包',
      // 默认显示进行中的活动
      tabIndex: 1,
      currentPage: 1,
      pageParams: {},

      activeName: 'first',
      actName: '',
      packName: '',
      accessMode: -1,
      get_type_option: [
        { value: -1, label: '全部' },
        { value: 0, label: '现金' },
        { value: 1, label: '积分' },
        { value: 2, label: '免费' }
      ],
      tableData: [],
      totalRows: null
    }
  },
  methods: {
    initDataList () {
      let param = {
        'state': parseInt(this.tabIndex),
        'accessMode': parseInt(this.accessMode),
        'actName': this.actName,
        'packName': this.packName,
        'currentPage': 1
      }

      couponPackageList(param).then((res) => {
        if (res.error === 0) {
          this.handleData(res.content.dataList)
          this.pageParams = res.content.page
        }
      })
    },

    // 表格数据处理
    handleData (data) {
      data.map((item, index) => {
        // TODO: 国际化
        switch (item.accessMode) {
          case 0:
            item.accessMode = '现金购买'
            break
          case 1:
            item.accessMode = '积分购买'
            break
          case 2:
            item.accessMode = '直接领取'
            break
        }
        item.vaildDate = `${item.startTime}至${item.endTime}`
        item.statusName = this.getActStatusString(item.status, item.startTime, item.endTime)
      })
      this.tableData = data
    },

    // 停用优惠券礼包活动
    puaseCouponPackage (id) {
      let param = {
        'id': id,
        'status': 0
      }

      updateCouponPackage(param).then((res) => {
        if (res.error === 0) {
          alert('停用成功')
          this.initDataList()
        }
      })
    },

    // 启用优惠券礼包活动
    enableCouponPackage (id) {
      let param = {
        'id': id,
        'status': 1
      }

      updateCouponPackage(param).then((res) => {
        if (res.error === 0) {
          alert('启用成功')
          this.initDataList()
        }
      })
    },

    // 删除优惠券礼包活动
    delCouponPackage (id) {
      let param = {
        'id': id
      }

      deleteCouponPackage(param).then((res) => {
        if (res.error === 0) {
          alert('删除成功')
          this.initDataList()
        }
      })
    },

    handleCurrentChange () {
      console.log(this.currentPage)
    },
    handleClick (tab) {
      this.initDataList(tab)
    },
    edit (scope) {
      console.log(scope)
    },
    addCouponPackage () {
      this.$router.push({
        name: 'coupon_Package_add'
      })
    }
  },
  watch: {
    'tabIndex' (n, o) {
      this.initDataList()
    }
  },
  mounted () {
    this.initDataList()
  }

}
</script>

